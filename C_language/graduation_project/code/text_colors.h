#ifndef      _TEXT_COLORS_H_
#define      _TEXT_COLORS_H_


/**
 * @brief  these macros changes the color of the text in the termenal
 * @note u can but the macro before the text and u notice the color is changed in the termenal 
 */

#define          __green__          "\33[1;32m"     
#define          __yellow__         "\33[1;33m"     
#define          __grey__           "\33[1;34m"     
#define          __red__            "\33[1;31m"     
#define          __black__          "\33[1;30m"     

#define          __bg_red__         "\33[41m"
#define          __bg_green__       "\33[42m"
#define          __bg_yellow__      "\33[43m"
#define          __bg_blue__        "\33[44m"


#endif  /*_TEXT_COLORS_H_*/